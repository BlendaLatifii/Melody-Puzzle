   <div class="biography">
    <p style="font-family: system-ui;font-size: large;">
       <b>Elvana Gjata</b> was born on 3 February 1987 in Tirana, Albania She started singing as a teenager and participated in various children's events. In 2001 she finished in 3rd place at the Festival of Young Voices with her song "Le të këndojmë së bashku" (Let's sing together).
        Her professional career started at "Ethet", a similar contest to Idols. After the talent show, she started releasing pop and dance songs and became one of the most influential singers in Albania. 
       She rose to fame during 2005 and 2006 and continues to be one of Albania's most popular singers.
       Gjata started working professionally since 2005 after participating in various festivals and releasing her own music. She debuted her first single called "Të kam xhan" (I love you) in Kënga Magjike. 
       The following year, Gjata was invited to work with Tingulli 3nt. They released "Të Dy" (Both of Us). The same year Gjata released her best-selling single to date, "Mamës" which became an instant hit. 
       That same year she also released "Pranë teje" (Near you) and "Zjarri që ti ndez" (The fire you light).
       Gjata won her first Videofest award in 2007 - she won <b>Best RnB Video with "Mamës" in 2007</b>, and during that year she released her first album, entitled "Mamës". Gjata also decided to take part in Kënga Magjike again with "Ku jeton dashuria ime" (Where Is My Love). 
       She advanced to the final round and ended third, behind winner Aurela Gaçe and future collaborator Flori Mumajesi featuring Soni Malaj. 
       In 2008, Gjata mainly focused on her album and she released "Vetes" (Myself) which became a very popular single. The album was later released with the same name.
       During 2009, Gjata collaborated with different artists from Albania and Kosovo and was one of the most active singers that year. She released "Nuk janë më" (Not anymore), "Hitech", "Turn U On" (which was nominated for Best Albanian Song that year at the Balkan Music Awards) and "Dhe Zemra Ndal" (My heart stops).
       The following year, Gjata released two summer songs, both becoming hits. She first released "A Ke Ti Zemër" (Do You Have a Heart?) and "Mamani Nejën" (Make My Night) with the Kosovar rapper Fugaa. They performed the song in Sofia, Bulgaria at the 2010 Balkan Music Awards. The song was also nominated for Best Albanian Song of the Year.
       Gjata entered the eighth edition of Top Fest with "Me Ty" (With You), a pop ballad which received positive feedback from the audience and music critics. Elvana used Top Fest also to promote her album. After her first appearance, she became the favorite artist to win and received a lot of media attention.
        On June 2011, Gjata was declared the winner. She mentioned that the victory was dedicated to her friend Elsina Hidersha better known as Emmy, an Albanian singer who died in a car accident that year.
       Soon after her win, Gjata released her third album Afër dhe Larg (Near and Far) in early 2011. The album contained singles, including "Me Ty" and other recent hits such as "Kudo Qe Jam" and the titular single "Afër dhe Larg", as well as singles that were released between 2009 and 2010, and brand new singles recorded especially for the new album.
       In 2012, Gjata's song "Afër dhe Larg" won at the Balkan Music Awards for Best Albanian Song of the Year and Best Music Video of the Year.
       Gjata only released one single that year. The single, called "Gjaku im" (My blood), which was accompanied with a music video, was made in order to commemorate the 100th Anniversary of the Independence of Albania. The music video premiered on April 28, 2012 and contained a strong message to the Albanian people.
       In early 2013, Gjata recorded five acoustic live sessions called: "Si unë" (Like Me), "Pak nga Pak" (A Little), "As Ti" (You Neither), "Mëngjesi i Kësaj Here" (The Morning of This Time) and "Anonim" (Anonymous).
       During summer, Gjata toured around the Albanian-speaking territories, as well as Switzerland, Germany and the Scandinavian countries.
