           <b>Melinda Ademi</b> is a well-known singer and songwriter. 
           She has surprised not only the jury of the famous television show "American Idol" but also all the viewers, not only for her wonderful voice, but also for the story she carries. 
           The 16-year-old from Mitrovica, Melinda Ademi, told her story from when the family decided to leave Kosovo, during the war, to her dreams of becoming a great singer. 
           In the video shown by 'Fox TV', the girl tells about the moments of her departure from Kosovo when she was quite young, although she remembers the shooting very well. 
           While her mother, she can't hold back her tears when she looks back at the memories, showing that it was quite difficult to leave everything behind. 
           'But how can you raise children in the middle of the war' - she says. 
           Winners of the American lottery, Melinda, together with her parents, two sisters and a brother, started life from scratch thus starting their American dream and making America their home. 
           Although it was difficult, they succeeded. 
           And now, Melinda is one of the standout contestants on 'American Idol', and left judges Steven Tyler and Jeniffer Lopez speechless when they curiously asked her where her unique name comes from. 
           "It is not possible to have such a star from Kosovo, but the truth is that a star is standing before my eyes," said Steven Tyler. 
           After she sang her song, the judges welcomed her to Hollywood with applause and smiles.

           Melinda Ademi is enjoying her first successes in the Albanian music scene thanks to some songs, which are very liked by her fans. 
           Through a post on social networks, the singer confessed that her love for music is great and that she has never given up. 
           "All my life I have loved music. 
           When I was 16 years old I went on 'American Idol', I didn't win and I went again two years later and I got to the semi-finals. 
           I didn't win the prize, but I didn't give up. 
           I went back to my hometown to make music, which I feel. 
           I fell on the stage and got back up, and I faced the difficulties. 
           I am proud of myself and it is a fact that you should never give up", Melinda wrote in a post on Instagram".
           
