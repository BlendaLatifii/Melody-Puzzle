           <b>Mozzik</b> Mozzik is a very popular singer and songwriter.
            He is very young in age, but Gramoz Aliu or as he is known to the public 'Mozzik', is one of the most energetic Albanian singers in the current of contemporary music. 
            He stands out in his unique style and the special texts he makes. 
            He is from Ferizaj and is the brother of Getoar Aliu - Getinjo. 
            The modern genre sleeps in his soul and Mozzik says that he will never change this style of singing. 
            He sang several songs that quickly became hits and received millions of hits. 
            The collaborations with Kid and Gensila turned out to be very popular. 
            Mozzik has had many offers to join different labels in Kosovo, but has refused the offers, staying loyal to the On Records label until January 2018. 
            After many disagreements, the moment of separation comes in the worst way. 
            Mozzik and Getinjo's hits are hidden from Zzap & Chris' Youtube channel. 
            Both parties have sued each other. 
            On one side we have Mozzik and Getinjo while on the other side we have Zzap & Chris, OnRecords and manager Elshani. 
            The other members of OnRecords, who were once considered brothers, have decided to continue with OnRecords. 
            Kida and Endri posted since the first day of the fight that they are part of OnRecords and are preparing new hits. 
