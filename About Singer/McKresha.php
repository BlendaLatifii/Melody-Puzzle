   <div class="biography">
    <p style="font-family: 30px;">
       <b>Kreshnik Fazliu</b> was born on 5 September 1984 into an Albanian family in the city of Mitrovicë, Kosovo. 
       Mc Kresha was already creating a big buzz around himself through his major single in 2010 "Lej Flleshat". 
       The song has become an all time classic in the Albanian hip hop scene. In 2011, Mc Kresha, Lyrical Son, Strike Beatz, K-Master & Onufër Berisha started their music label P.I.N.T ( Për Inati T'Njoni Tjetrit ).
       The label has now several artists & producers signed to their name. In July 2018, Fazliu was announced as an act at the Sunny Hill Festival on its debut edition in Pristina. In August 2019, he was approached to perform for a second time at the Sunny Hill Festival.
       Fazliu's fourth collaborative album, Muzikë e Alltisë, was released in January 2022. Songs featured on the record included "Hotel Mahalla", "Paret Lejla", "Rammstein" and "Stuhi mbi oqean" featuring Albanian singer Elvana Gjata, which attained commercial success in Albania.
