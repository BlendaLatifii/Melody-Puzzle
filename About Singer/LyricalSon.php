   <div class="biography">
    <p style="font-family: system-ui;font-size: large;">
       <b>Lyrical Son</b> was born as Festim Arifi on 28 January 1984 into an Albanian family in the city of Pristina, Kosovo.
       Arifi began rapping at high school in his youth and started pursuing a music career seriously, after the Kosovo War in 1999.
       During his elementary school years, he eventually adopted his stage name, which was taken after a notebook entry from his English teacher.
       Lyrical Son started to become a household name in the Albanian hip hop scene in the early 2000s with his catchy hooks and creative writing and flows on his verses. In October 19 2008, he released his first solo album titled "Recept" . 
       The outstanding song from the album is the all time classic "Po Pritoj". 3 years laters, Lyrical Son together with Mc Kresha , Strike Beatz, K-Master and Onufër Berisha started the music label P.I.N.T ( Për Inati T'Njoni Tjetrit ).
       The music label has become one of the most prominent labels in the Albanian hip hop scene with having several successful albums, signing young artists & producers and elevating their careers.
