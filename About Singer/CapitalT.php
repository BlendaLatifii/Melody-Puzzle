   <div class="biography">
    <p style="font-family: system-ui;font-size: large;">
       <b>Trim Ademi</b> was born on 29 February 1992 into an Albanian family in the city of Pristina,Kosovo. His father, Agim Ademi, is a former professional football player and current president of the Football Federation of Kosovo.
        Ademi attended the Asim Vokshi Elementary School and enrolled, upon his graduation, at the Sami Frashëri High School in Pristina.
        Ademi commenced his early music career in 2008 and debuted with the single "Shopping" by 2po2 featuring Capital T. 
        The same year, he released his second single "1 Mëngjes" in collaboration with Albanian singer Adelina Thaqi. The following year, he released almost three singles including "Shum Nalt" in collaboration with 2po2 and Albanian singer Dafina Zeqiri. 
        In 2010, he announced his debut studio album Replay and released the record in the same year. That year, he participated with Eni Koçi in the 11th edition of Kënga Magjike with the song "Diva". 
        The singer released his debut studio album "Replay" in 2010, his second album "Kapo" in 2012 which was followed by his third album "Slumdog Millionaire" in 2015. His fourth album "Winter Is Here" was released through Authentic Entertainment in 2017 and achieved commercial success in Switzerland whereas it debuted on the Swiss Album Charts at number sixty-nine.
        In June 2016, he won the award for the "Video of the Year" at the Top Music Awards 2016.
