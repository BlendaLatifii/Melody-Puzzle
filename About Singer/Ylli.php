   <div class="biography">
    <p style="font-family: system-ui;font-size: large;">
       <b>Yll Limani</b> was born on 24 September 1994 into an Albanian family in the city of Pristina ,Kosovo.
         His father, Mufail Limani, was a member of the Albanian band Minatori. 
        The singer's public singing debut was at the first season of The Voice of Albania in 2011, followed by an appearance on the 9th edition of the Albanian music contest Top Fest in 2012.
        Upon a three-year hiatus, Limani's first single as a lead artist, "Single" featuring Kosovo-Albanian rapper Gent Fatali, was released in January 2015.
        The singer made his first appearance on Albania's Top 100 chart with "Pritëm se po vi" in July 2016, which debuted and peaked within the top three. 
        Following this, the Albanian broadcaster, Radio Televizioni Shqiptar (RTSH), reported in November 2016 that Limani was selected among the 25 contestants to compete in the 55th edition of Festivali i Këngës in December with the song "Shiu".
        Finishing in third place, the single peaked at number 34 in March 2017 as his second charting record in Albania.
        In July 2019, he scored his second number-one single with the release of "Nuk po kalojka", a collaboration with composers Aida Baraku, Kevin Field, Armend Rexhepagiqi alongside Macedonian producer Darko Dimitrov.
        The Sunny Hill Festival in Pristina approached Limani a month later, and went on to perform at the festival sharing stage with other acclaimed artists, such as Miley Cyrus, Calvin Harris and Dua Lipa.
