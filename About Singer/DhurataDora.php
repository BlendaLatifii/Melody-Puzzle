   <div class="biography">
    <p style="font-family: system-ui;font-size: large;">
       <b>Dhurata Murturi</b> was born into an Albanian family in the city of Nuremberg in Bavaria, Germany.
       She went to elementary school in the town of Fürth. She started singing as a young woman under the name Dhurata Dora. She began her music career in Kosovo, where she released her first single and music video in 2011, "Vete kërkove". 
       Following success from her first single, she collaborated with Don Arbas, releasing "Get Down" in 2012. The song proved to be even more popular than her first. The following year she launched "I Like Dat".
        In summer of 2013, she released the hit song "Edhe Pak", in a collaboration with Blunt & Real and Lumi B.
       In November 2014, Dora's single "A bombi" was released. During spring of 2015, Dora began working with production company Max Production Albania.
       In April 2019, she premiered her international breakthrough single "Zemër" in collaboration with Algerian rapper Soolking.The single reached number one in Albania and entered the music chart in Belgium, Switzerland and France where it went on to be certified platinum by the Syndicat National de l'Édition Phonographique (SNEP).
        As of July 2022 it counts more than 714 million views on YouTube. 
        In February 2020, she collaborated with German-Albanian rapper Azet on the German-language single "Lass los". She also collaborated with Romanian singer Inna and British rapper Stefflon Don on the single "Yummy" in 2023.
